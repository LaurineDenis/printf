#include "libft.h"

char	*ft_strdup(const char *s1)
{
	size_t		i;
	size_t		j;
	char		*str;

	i = ft_strlen(s1);
	j = 0;
	if (!(str = (char*)malloc(sizeof(char) * i + 1)))
		return (0);
	while (j < i)
	{
		str[j] = s1[j];
		j++;
	}
	str[j] = '\0';
	return (str);
}
